/**
 * Invoice Generator Service
 * Handles invoice creation, calculation, and PDF generation
 */

import type { Invoice, InvoiceStatus } from '@/types/database';
import { BRAND_NAME } from '@/lib/brand';

export interface InvoiceData {
    studentName: string;
    courseName: string;
    rtoName: string;
    tuitionFee: number;
    materialFee: number;
    applicationFee: number;
    otherFees?: number;
    discount?: number;
    dueDate?: string;
    notes?: string;
    applicationId?: string;
    partnerId?: string;
}

export interface InvoiceLineItem {
    description: string;
    amount: number;
}

export interface GeneratedInvoice {
    invoiceNumber: string;
    date: string;
    dueDate: string;
    studentName: string;
    courseName: string;
    rtoName: string;
    lineItems: InvoiceLineItem[];
    subtotal: number;
    discount: number;
    total: number;
    status: InvoiceStatus;
    notes?: string;
}

/**
 * Calculate invoice totals from fee components
 */
export function calculateInvoiceTotals(data: InvoiceData): {
    subtotal: number;
    discount: number;
    total: number;
    lineItems: InvoiceLineItem[];
} {
    const lineItems: InvoiceLineItem[] = [];

    if (data.tuitionFee > 0) {
        lineItems.push({ description: 'Tuition Fee', amount: data.tuitionFee });
    }
    if (data.materialFee > 0) {
        lineItems.push({ description: 'Material Fee', amount: data.materialFee });
    }
    if (data.applicationFee > 0) {
        lineItems.push({ description: 'Application Fee', amount: data.applicationFee });
    }
    if (data.otherFees && data.otherFees > 0) {
        lineItems.push({ description: 'Other Fees', amount: data.otherFees });
    }

    const subtotal = lineItems.reduce((sum, item) => sum + item.amount, 0);
    const discount = data.discount || 0;
    const total = Math.max(0, subtotal - discount);

    return { subtotal, discount, total, lineItems };
}

/**
 * Generate invoice data structure for PDF rendering
 */
export function generateInvoiceData(
    invoiceNumber: string,
    data: InvoiceData
): GeneratedInvoice {
    const { subtotal, discount, total, lineItems } = calculateInvoiceTotals(data);
    const today = new Date();
    const dueDate = data.dueDate
        ? new Date(data.dueDate)
        : new Date(today.getTime() + 30 * 24 * 60 * 60 * 1000); // 30 days from now

    return {
        invoiceNumber,
        date: today.toISOString().split('T')[0],
        dueDate: dueDate.toISOString().split('T')[0],
        studentName: data.studentName,
        courseName: data.courseName,
        rtoName: data.rtoName,
        lineItems,
        subtotal,
        discount,
        total,
        status: 'draft',
        notes: data.notes,
    };
}

/**
 * Format currency for display
 */
export function formatCurrency(amount: number, currency = 'AUD'): string {
    return new Intl.NumberFormat('en-AU', {
        style: 'currency',
        currency,
    }).format(amount);
}

/**
 * Generate invoice number (client-side preview, actual generated by DB)
 */
export function generatePreviewInvoiceNumber(): string {
    const year = new Date().getFullYear();
    const random = Math.floor(Math.random() * 9999)
        .toString()
        .padStart(4, '0');
    return `INV-${year}-${random}`;
}

