import { createClient } from '@/lib/supabase/client';
import { ACTIVE_RECORD_FILTER } from '@/lib/soft-delete';
import type { BulkOperation } from '@/types/database';

export interface BulkInvoiceOptions {
    dueInDays?: number;
    notes?: string;
    invoiceType?: 'customer' | 'agent';
    autoSyncXero?: boolean;
}

async function isXeroConnected(): Promise<boolean> {
    try {
        const response = await fetch('/api/xero/status');
        if (!response.ok) return false;
        const data = await response.json();
        return Boolean(data?.connected);
    } catch {
        return false;
    }
}

async function trySyncInvoiceToXero(invoiceId: string): Promise<void> {
    try {
        await fetch('/api/xero/sync', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ action: 'sync_invoice', invoiceId }),
        });
    } catch (error) {
        console.warn('Auto-sync invoice to Xero failed:', error);
    }
}

/**
 * Create a single invoice in the database
 */
export async function createInvoice(
    data: InvoiceData,
    options: BulkInvoiceOptions = {}
): Promise<Invoice | null> {
    const supabase = createClient();
    const { subtotal, discount, total } = calculateInvoiceTotals(data);

    const { data: invoice, error } = await supabase
        .from('invoices')
        .insert({
            application_id: data.applicationId || null,
            partner_id: data.partnerId || null,
            student_name: data.studentName,
            course_name: data.courseName,
            rto_name: data.rtoName,
            tuition_fee: data.tuitionFee || 0,
            material_fee: data.materialFee || 0,
            application_fee: data.applicationFee || 0,
            other_fees: data.otherFees || 0,
            discount: discount,
            subtotal: subtotal,
            total_amount: total,
            due_date: data.dueDate || null,
            notes: data.notes || null,
        })
        .select()
        .single();

    if (error) {
        console.error('Error creating invoice:', error);
        return null;
    }

    if (options.autoSyncXero) {
        const connected = await isXeroConnected();
        if (connected && invoice?.id) {
            await trySyncInvoiceToXero(invoice.id);
        }
    }

    return invoice;
}

/**
 * Create bulk invoices for multiple applications
 */
export async function createBulkInvoices(
    applicationIds: string[],
    options: BulkInvoiceOptions = {}
): Promise<{ success: boolean; operation?: BulkOperation; error?: string }> {
    const supabase = createClient();

    // Get current user
    const { data: { user } } = await supabase.auth.getUser();
    if (!user) {
        return { success: false, error: 'Not authenticated' };
    }

    // Create bulk operation record
    const { data: operation, error: opError } = await supabase
        .from('bulk_operations')
        .insert({
            type: 'invoice',
            status: 'processing',
            total_items: applicationIds.length,
            created_by: user.id,
        })
        .select()
        .single();

    if (opError) {
        return { success: false, error: 'Failed to create bulk operation' };
    }

    // Fetch applications with their offerings
    const { data: applications, error: appError } = await supabase
        .from('applications')
        .select(`
            id,
            student_first_name,
            student_last_name,
            quoted_tuition,
            quoted_materials,
            partner_id,
            offering:rto_offerings(
                tuition_fee_onshore,
                material_fee,
                application_fee,
                qualification:qualifications(name),
                rto:rtos(name)
            )
        `)
        .or(ACTIVE_RECORD_FILTER)
        .in('id', applicationIds);

    if (appError || !applications) {
        await supabase
            .from('bulk_operations')
            .update({ status: 'failed' })
            .eq('id', operation.id);
        return { success: false, error: 'Failed to fetch applications' };
    }

    let processed = 0;
    let failed = 0;
    const errorLog: Array<{ item_id: string; error: string }> = [];
    const createdInvoiceIds: string[] = [];
    const dueDate = new Date();
    dueDate.setDate(dueDate.getDate() + (options.dueInDays || 30));

    // Create invoices for each application
    for (const app of applications) {
        try {
            // Supabase returns nested relations as objects (not arrays) for single foreign key
            const offering = app.offering as unknown as {
                tuition_fee_onshore: number;
                material_fee: number;
                application_fee: number;
                qualification: { name: string } | null;
                rto: { name: string } | null;
            } | null;

            const tuitionFee = app.quoted_tuition || offering?.tuition_fee_onshore || 0;
            const materialFee = app.quoted_materials || offering?.material_fee || 0;
            const applicationFee = offering?.application_fee || 0;
            const total = tuitionFee + materialFee + applicationFee;

            const { data: createdInvoice, error } = await supabase
                .from('invoices')
                .insert({
                    application_id: app.id,
                    partner_id: app.partner_id || null,
                    student_name: `${app.student_first_name} ${app.student_last_name}`,
                    course_name: offering?.qualification?.name || 'Course',
                    rto_name: offering?.rto?.name || 'RTO',
                    tuition_fee: tuitionFee,
                    material_fee: materialFee,
                    application_fee: applicationFee,
                    other_fees: 0,
                    discount: 0,
                    subtotal: total,
                    total_amount: total,
                    due_date: dueDate.toISOString().split('T')[0],
                    notes: options.notes || null,
                    invoice_type: options.invoiceType || 'customer',
                })
                .select('id')
                .single();

            if (error) {
                failed++;
                errorLog.push({ item_id: app.id, error: error.message });
            } else {
                processed++;
                if (createdInvoice?.id) createdInvoiceIds.push(createdInvoice.id);
            }
        } catch {
            failed++;
            errorLog.push({ item_id: app.id, error: 'Unknown error' });
        }
    }

    if (options.autoSyncXero) {
        const connected = await isXeroConnected();
        if (connected) {
            for (const invoiceId of createdInvoiceIds) {
                await trySyncInvoiceToXero(invoiceId);
            }
        }
    }

    // Update operation status
    await supabase
        .from('bulk_operations')
        .update({
            status: failed === applicationIds.length ? 'failed' : 'completed',
            processed_items: processed,
            failed_items: failed,
            error_log: errorLog,
            completed_at: new Date().toISOString(),
        })
        .eq('id', operation.id);

    return {
        success: true,
        operation: {
            ...operation,
            processed_items: processed,
            failed_items: failed,
            status: failed === applicationIds.length ? 'failed' : 'completed',
        } as BulkOperation,
    };
}

/**
 * Get all invoices with filters
 */
export async function getInvoices(filters?: {
    status?: InvoiceStatus;
    partnerId?: string;
    fromDate?: string;
    toDate?: string;
}): Promise<Invoice[]> {
    const supabase = createClient();

    const buildQuery = (options: { includeDeletedFilter: boolean; includeRelations: boolean }) => {
        let query = supabase
            .from('invoices')
            .select(options.includeRelations
                ? '*,partner:partners(id,company_name),application:applications(id,application_number,student_uid)'
                : '*');

        if (options.includeDeletedFilter) {
            query = query.or('is_deleted.is.null,is_deleted.eq.false');
        }

        if (filters?.status) {
            query = query.eq('status', filters.status);
        }
        if (filters?.partnerId) {
            query = query.eq('partner_id', filters.partnerId);
        }
        if (filters?.fromDate) {
            query = query.gte('created_at', filters.fromDate);
        }
        if (filters?.toDate) {
            query = query.lte('created_at', filters.toDate);
        }

        return query.order('created_at', { ascending: false });
    };

    const isMissingIsDeletedColumn = (message: string) => (
        message.includes('is_deleted') && (message.includes('column') || message.includes('schema cache'))
    );

    const isRelationOrPermissionError = (message: string) => (
        message.includes('relationship')
        || message.includes('foreign key')
        || message.includes('could not find')
        || message.includes('permission denied')
    );

    let { data, error } = await buildQuery({ includeDeletedFilter: true, includeRelations: true });

    const message = (error?.message || '').toLowerCase();

    if (error && isMissingIsDeletedColumn(message)) {
        const fallback = await buildQuery({ includeDeletedFilter: false, includeRelations: true });
        data = fallback.data;
        error = fallback.error;
    }

    const messageAfterDeletedFallback = (error?.message || '').toLowerCase();
    if (error && isRelationOrPermissionError(messageAfterDeletedFallback)) {
        let fallback = await buildQuery({ includeDeletedFilter: true, includeRelations: false });

        const fallbackMessage = (fallback.error?.message || '').toLowerCase();
        if (fallback.error && isMissingIsDeletedColumn(fallbackMessage)) {
            fallback = await buildQuery({ includeDeletedFilter: false, includeRelations: false });
        }

        data = fallback.data;
        error = fallback.error;
    }

    if (error) {
        console.error('Error fetching invoices:', error);
        return [];
    }

    const invoices = (data || []) as unknown as Array<Invoice & { is_deleted?: boolean | null }>;

    return invoices
        .filter((invoice) => {
            return invoice.is_deleted !== true;
        });
}

/**
 * Update invoice status
 */
export async function updateInvoiceStatus(
    id: string,
    status: InvoiceStatus,
    paymentInfo?: { reference?: string; method?: string }
): Promise<boolean> {
    const supabase = createClient();

    const updateData: Record<string, unknown> = { status };

    if (status === 'paid') {
        updateData.paid_at = new Date().toISOString();
        if (paymentInfo?.reference) updateData.payment_reference = paymentInfo.reference;
        if (paymentInfo?.method) updateData.payment_method = paymentInfo.method;
    }

    const { error } = await supabase
        .from('invoices')
        .update(updateData)
        .eq('id', id);

    return !error;
}

/**
 * Get invoice by ID
 */
export async function getInvoiceById(id: string): Promise<Invoice | null> {
    const supabase = createClient();

    const { data, error } = await supabase
        .from('invoices')
        .select('*, partner:partners(*), application:applications(*)')
        .eq('id', id)
        .single();

    if (error) {
        console.error('Error fetching invoice:', error);
        return null;
    }

    return data;
}

/**
 * Delete invoice (soft delete)
 */
export async function deleteInvoice(id: string): Promise<boolean> {
    const supabase = createClient();
    const { data: { user } } = await supabase.auth.getUser();

    const { error } = await supabase
        .from('invoices')
        .update({
            is_deleted: true,
            deleted_at: new Date().toISOString(),
            deleted_by: user?.id || null,
        })
        .eq('id', id);

    return !error;
}

