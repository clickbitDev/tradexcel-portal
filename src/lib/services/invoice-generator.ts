/**
 * Convert database Invoice record to GeneratedInvoice format
 */
export function invoiceToGenerated(invoice: Invoice): GeneratedInvoice {
    const lineItems: InvoiceLineItem[] = [];

    if (invoice.tuition_fee > 0) {
        lineItems.push({ description: 'Tuition Fee', amount: invoice.tuition_fee });
    }
    if (invoice.material_fee > 0) {
        lineItems.push({ description: 'Material Fee', amount: invoice.material_fee });
    }
    if (invoice.application_fee > 0) {
        lineItems.push({ description: 'Application Fee', amount: invoice.application_fee });
    }
    if (invoice.other_fees > 0) {
        lineItems.push({ description: 'Other Fees', amount: invoice.other_fees });
    }

    const subtotal = lineItems.reduce((sum, item) => sum + item.amount, 0);

    return {
        invoiceNumber: invoice.invoice_number,
        date: invoice.created_at.split('T')[0],
        dueDate: invoice.due_date || '',
        studentName: invoice.student_name,
        courseName: invoice.course_name || '',
        rtoName: invoice.rto_name || '',
        lineItems,
        subtotal,
        discount: invoice.discount,
        total: invoice.total_amount,
        status: invoice.status,
        notes: invoice.notes || undefined,
    };
}

