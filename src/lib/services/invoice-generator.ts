/**
 * Send single invoice email
 */
export async function sendInvoiceEmail(
    invoiceId: string,
    recipientType: 'student' | 'agent'
): Promise<InvoiceSendResult> {
    const supabase = createClient();
    const { data: { user } } = await supabase.auth.getUser();

    // Fetch invoice with relations
    const { data: invoice, error: fetchError } = await supabase
        .from('invoices')
        .select(`
            *,
            application:applications(
                student_email,
                student_first_name,
                student_last_name,
                partner:partners(contact_email, company_name)
            )
        `)
        .eq('id', invoiceId)
        .single();

    if (fetchError || !invoice) {
        return { success: false, invoiceId, recipient: '', error: 'Invoice not found' };
    }

    const app = invoice.application as {
        student_email: string | null;
        student_first_name: string;
        student_last_name: string;
        partner: { contact_email: string | null; company_name: string } | null;
    } | null;

    // Determine recipient
    let recipientEmail: string | null = null;
    let recipientName: string = '';

    if (recipientType === 'student') {
        recipientEmail = app?.student_email || null;
        recipientName = `${app?.student_first_name || ''} ${app?.student_last_name || ''}`.trim();
    } else {
        recipientEmail = app?.partner?.contact_email || null;
        recipientName = app?.partner?.company_name || 'Agent';
    }

    if (!recipientEmail) {
        return {
            success: false,
            invoiceId,
            recipient: '',
            error: `No ${recipientType} email available`
        };
    }

    // Queue the email
    const { error: queueError } = await supabase
        .from('notification_queue')
        .insert({
            channel: 'email',
            recipient: recipientEmail,
            subject: `Invoice ${invoice.invoice_number} - ${invoice.student_name}`,
            body: `
Dear ${recipientName},

Please find attached invoice ${invoice.invoice_number} for ${invoice.student_name}.

Amount Due: ${formatCurrency(invoice.total_amount)}
Due Date: ${invoice.due_date || 'Upon receipt'}

If you have any questions regarding this invoice, please contact us.

Best regards,
${BRAND_NAME}
            `.trim(),
            invoice_id: invoiceId,
            application_id: invoice.application_id,
            status: 'pending',
            scheduled_at: new Date().toISOString(),
            created_by: user?.id || null,
            metadata: {
                invoice_number: invoice.invoice_number,
                recipient_type: recipientType,
                amount: invoice.total_amount,
            },
        });

    if (queueError) {
        return { success: false, invoiceId, recipient: recipientEmail, error: queueError.message };
    }

    // Update invoice sent tracking
    await supabase
        .from('invoices')
        .update({
            sent_at: new Date().toISOString(),
            sent_to: recipientEmail,
            sent_via: 'email',
            send_count: (invoice.send_count || 0) + 1,
            status: invoice.status === 'draft' ? 'sent' : invoice.status,
        })
        .eq('id', invoiceId);

    // Log to notification_logs
    await supabase
        .from('notification_logs')
        .insert({
            channel: 'email',
            recipient: recipientEmail,
            recipient_name: recipientName,
            subject: `Invoice ${invoice.invoice_number}`,
            status: 'sent',
            sent_by: user?.id || null,
            invoice_id: invoiceId,
            message_type: 'invoice',
        });

    return { success: true, invoiceId, recipient: recipientEmail };
}

/**
 * Send bulk invoice emails - groups by agent for efficiency
 */
export async function sendBulkInvoiceEmails(
    invoiceIds: string[],
    sendTo: 'student' | 'agent' = 'agent'
): Promise<{
    success: boolean;
    sent: number;
    failed: number;
    results: InvoiceSendResult[];
}> {
    const supabase = createClient();
    const { data: { user } } = await supabase.auth.getUser();

    // Fetch all invoices with relations
    const { data: invoices, error } = await supabase
        .from('invoices')
        .select(`
            *,
            partner:partners(id, contact_email, company_name),
            application:applications(
                id,
                student_email,
                student_first_name,
                student_last_name
            )
        `)
        .in('id', invoiceIds);

    if (error || !invoices) {
        return { success: false, sent: 0, failed: invoiceIds.length, results: [] };
    }

    const results: InvoiceSendResult[] = [];

    if (sendTo === 'agent') {
        // Group invoices by agent for batch sending
        const invoicesByAgent = new Map<string, typeof invoices>();

        for (const inv of invoices) {
            const partner = inv.partner as { id: string; contact_email: string | null; company_name: string } | null;
            const agentId = partner?.id || 'no-agent';

            if (!invoicesByAgent.has(agentId)) {
                invoicesByAgent.set(agentId, []);
            }
            invoicesByAgent.get(agentId)!.push(inv);
        }

        // Send one email per agent with all their students' invoices
        for (const [agentId, agentInvoices] of invoicesByAgent) {
            const partner = agentInvoices[0].partner as {
                contact_email: string | null;
                company_name: string
            } | null;

            if (!partner?.contact_email) {
                for (const inv of agentInvoices) {
                    results.push({
                        success: false,
                        invoiceId: inv.id,
                        recipient: '',
                        error: 'No agent email',
                    });
                }
                continue;
            }

            // Create summary email for agent
            const invoiceList = agentInvoices.map(inv =>
                `• ${inv.invoice_number}: ${inv.student_name} - ${formatCurrency(inv.total_amount)}`
            ).join('\n');

            const totalAmount = agentInvoices.reduce((sum, inv) => sum + (inv.total_amount || 0), 0);

            const { error: queueError } = await supabase
                .from('notification_queue')
                .insert({
                    channel: 'email',
                    recipient: partner.contact_email,
                    subject: `${agentInvoices.length} Invoice(s) for Your Students`,
                    body: `
Dear ${partner.company_name},

Please find below the invoices for your students:

${invoiceList}

Total Amount: ${formatCurrency(totalAmount)}

If you have any questions regarding these invoices, please contact us.

Best regards,
${BRAND_NAME}
                    `.trim(),
                    status: 'pending',
                    scheduled_at: new Date().toISOString(),
                    created_by: user?.id || null,
                    metadata: {
                        invoice_ids: agentInvoices.map(i => i.id),
                        agent_id: agentId,
                        total_amount: totalAmount,
                    },
                });

            if (queueError) {
                for (const inv of agentInvoices) {
                    results.push({
                        success: false,
                        invoiceId: inv.id,
                        recipient: partner.contact_email,
                        error: queueError.message,
                    });
                }
            } else {
                // Update all invoices for this agent
                for (const inv of agentInvoices) {
                    await supabase
                        .from('invoices')
                        .update({
                            sent_at: new Date().toISOString(),
                            sent_to: partner.contact_email,
                            sent_via: 'email',
                            send_count: (inv.send_count || 0) + 1,
                            status: inv.status === 'draft' ? 'sent' : inv.status,
                        })
                        .eq('id', inv.id);

                    results.push({
                        success: true,
                        invoiceId: inv.id,
                        recipient: partner.contact_email,
                    });
                }

                // Log the batch send
                await supabase
                    .from('notification_logs')
                    .insert({
                        channel: 'email',
                        recipient: partner.contact_email,
                        recipient_name: partner.company_name,
                        subject: `${agentInvoices.length} Invoice(s)`,
                        status: 'sent',
                        sent_by: user?.id || null,
                        message_type: 'invoice',
                    });
            }
        }
    } else {
        // Send individual emails to students
        for (const inv of invoices) {
            const result = await sendInvoiceEmail(inv.id, 'student');
            results.push(result);
        }
    }

    const sent = results.filter(r => r.success).length;
    const failed = results.filter(r => !r.success).length;

    return { success: failed === 0, sent, failed, results };
}

/**
 * Check which applications already have invoices
 */
export async function getInvoicesByApplicationIds(
    applicationIds: string[]
): Promise<Map<string, Invoice>> {
    const supabase = createClient();

    const { data, error } = await supabase
        .from('invoices')
        .select('*')
        .in('application_id', applicationIds)
        .eq('is_deleted', false);

    if (error) {
        console.error('Error fetching invoices by application:', error);
        return new Map();
    }

    const map = new Map<string, Invoice>();
    for (const inv of data || []) {
        if (inv.application_id) {
            map.set(inv.application_id, inv);
        }
    }

    return map;
}
